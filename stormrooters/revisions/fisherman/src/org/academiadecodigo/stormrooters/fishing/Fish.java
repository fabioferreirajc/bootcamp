package org.academiadecodigo.stormrooters.fishing;

public class Fish {


    private int size;
    private int weight;

    public Fish(int size, int weight) {

        this.size = size;
        this.weight = weight;

    }


    public int getSize() {
        return size;
    }

    public int getWeight(){
        return weight;
    }


}
